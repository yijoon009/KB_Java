
public class PhoneSystem {
	String name;
	String gradeCode;
	int unitPrice;
	int basicPrice;
	int useTime;
	int discountRate;
	int total;
	
	public PhoneSystem() {;}
	
	public PhoneSystem(String name, String gradeCode, int unitPrice, int basicPrice, int useTime, int discountRate,
			int total) {
		super();
		this.name = name;
		this.gradeCode = gradeCode;
		this.unitPrice = unitPrice;
		this.basicPrice = basicPrice;
		this.useTime = useTime;
		this.discountRate = discountRate;
		this.total = total;
	}
	
	
	
}
